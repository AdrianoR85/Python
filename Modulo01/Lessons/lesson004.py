# Tipo de dado bool
"""
  - Ao questionar algo em um programa, só existem duas respostas possíveis:
  Sim(True) ou não(False).
  - Existem vários operadores para "questionario". Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro.
"""
print(10 == 10)
print(10 == 11)
print(type(10 == 11))
print(-10)
